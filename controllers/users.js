// --------------------------------------------------------------Le controller utilisateur------------------------------------------------------


const bcrypt = require("bcrypt");
const User = require("../models/users");
const jwt = require("jsonwebtoken");


// Le controller permttant la création d'un utilisateur avec le hash du pot de passe afin de sécuriser l'accés et les données confideentielles
exports.signup = (req, res, next) => {
  bcrypt
    .hash(req.body.password, 10)
    .then((hash) => {
      const user = new User({
        email: req.body.email,
        password: hash,
      });
      user
        .save()
        .then(() => res.status(201).json({ message: "Utilisateur créé !" }))
        .catch((error) => res.status(400).json({ error }));
    })
    .catch((error) => res.status(500).json({ error }));
};


// Le controller de connection incluant le token afin de lier systematiquement notre utilisateur sur l'ensemble du parcours 
exports.login = (req, res, next) => {
  User.findOne({ email: req.body.email })
    .then((user) => {
      if (!user) {
        return res.status(401).json({ error: "Utilisateur non trouvé !" });
      }
      bcrypt
        .compare(req.body.password, user.password)
        .then((valid) => {
          if (!valid) {
            return res.status(401).json({ error: "Mot de passe incorrect !" });
          }
          res.status(200).json({
            userId: user._id,
            token: jwt.sign({ userId: user._id }, `${process.env.RANDOM_TOKEN_SECRET}`, {
              expiresIn: "2h",
            }),
          });
        })
        .catch((error) => res.status(500).json({ error }));
    })
    .catch((error) => res.status(500).json({ error }));
};
